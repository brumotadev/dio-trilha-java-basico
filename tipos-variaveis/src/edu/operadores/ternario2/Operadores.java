package edu.operadores.ternario2;

public class Operadores {

    public static void main(String[] args) {
        int numero =  5;

        // numero = numero + 2;
        numero++;

        System.out.println(++numero);

        System.out.println(numero);

        numero--;
        System.out.println(numero--);


        boolean variavel = true;
        System.out.println(!variavel); // nega o valor, vira false
    }

}
